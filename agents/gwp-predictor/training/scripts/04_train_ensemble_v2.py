im = ax.imshow(pivot.values, cmap="viridis_r", aspect="auto")
ax.set_xticks(range(len(pivot.columns)))
ax.set_xticklabels(pivot.columns)
ax.set_yticks(range(len(pivot.index)))
ax.set_yticklabels(pivot.index)
ax.set_xlabel("hidden_dim")
ax.set_ylabel("depth")
ax.set_title("Hyperopt: best CV MAE log10(GWP100) per (hidden, depth)")
for i in range(len(pivot.index)):
    for j in range(len(pivot.columns)):
        v = pivot.values[i, j]
        ax.text(j, i, f"{v:.3f}", ha="center", va="center", color="white" if v > pivot.values.mean() else "black")
plt.colorbar(im, ax=ax, label="MAE log10(GWP)")
plt.tight_layout()
plt.savefig(SWEEP_DIR / "hyperopt_heatmap.png", dpi=120, bbox_inches="tight")
plt.close()


final_summary = {
    "n_train": int(len(train_df)),
    "n_holdout_reserved_untouched": int(len(holdout_df)),
    "n_features": len(FEATURE_NAMES),
    "ensemble_size": 5,
    "accelerator": ACCELERATOR,
    "best_config": best_cfg,
    "v1_baseline_mae": 0.570,
    "v2_with_features_and_hyperopt_mae": best_cfg["cv_mae_log10_gwp"],
    "improvement_pct": float(100 * (0.570 - best_cfg["cv_mae_log10_gwp"]) / 0.570),
    "target_mae_met": best_cfg["cv_mae_log10_gwp"] < 0.4,
    "sweep_total_minutes": round(sweep_total_elapsed / 60, 1),
    "sweep_top5": sweep_df.head(5)[["config", "mae_log10_gwp", "r2_log10_gwp", "mae_log10_lifetime"]].to_dict("records"),
}
with (OUTPUT_DIR / "final_results.json").open("w") as f:
    json.dump({"status": "completed", "summary": final_summary}, f, indent=2, default=str)

log.info("=" * 70)
log.info("DONE")
for k, v in final_summary.items():
    log.info(f"  {k}: {v}")
